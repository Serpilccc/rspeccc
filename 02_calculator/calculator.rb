def add(a,b)
   c=a+b
    return c
end

def substract(a,b)
    c=a-b
    return c

def sum(d)
   c=0
    d.each do |a|
        c+=a
    end
    return c
end
    
def multiply(d)
    c=1
    d.each do |a|
        c *= a
    end
    return c
    end

    
def power(a,b)
    c=a**b
    return c
end
    
